function displayException(err, focus)

  cprintf ( 'Errors', '\n[[%s]]  %s\n'    ...
          , err.identifier, err.message   ...
          );

  width   = max(cellfun(@length, {err.stack.name}));
  for iStack = numel(err.stack):-1:1
    fprintf ( ' |  %*s in <a href="matlab:edit(''%s''); matlab.desktop.editor.getActive().goToLine(%d);">%s:%d</a>\n'  ...
            , width                       ...
            , err.stack(iStack).name      ...
            , err.stack(iStack).file      ...
            , err.stack(iStack).line      ...
            , err.stack(iStack).file      ...
            , err.stack(iStack).line      ...
            );
  end
  
  fprintf('\n');
  
  if nargin > 1 && focus
    beep;
    commandwindow;
  end

end
