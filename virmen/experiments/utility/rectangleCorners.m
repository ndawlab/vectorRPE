%% RECTANGLECORNERS   Returns patch x and y coordinates for a rectangle with corner at (x,y) and with the given width/height
function [cornerX, cornerY] = rectangleCorners(x, y, width, height)

  % Canonical format of arguments
  x         = rowvec(x);
  y         = rowvec(y);
  width     = rowvec(width);
  height    = rowvec(height);
  
  
  cornerX   = [ x             ...
              ; x + width     ...
              ; x + width     ...
              ; x             ...
              ];
  cornerY   = [ y + height    ...
              ; y + height    ...
              ; y             ...
              ; y             ...
              ];

end
